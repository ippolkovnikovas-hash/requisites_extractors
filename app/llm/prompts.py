# ---------------------------------------------------------------------------
# Реестр промптов
# ---------------------------------------------------------------------------

PROMPTS: dict[str, str] = {
    "v1": EXTRACTION_PROMPT_V1,
    "v2": EXTRACTION_PROMPT_V2,
    "v3": EXTRACTION_PROMPT_V3,
    "image": EXTRACTION_PROMPT_IMAGE,
}

DEFAULT_VERSION = "v1"


def get_prompt(version: str, document_text: str) -> str:
    if version not in PROMPTS:
        raise ValueError(
            f"Unknown prompt version: '{version}'. "
            f"Available: {list(PROMPTS.keys())}"
        )
    return PROMPTS[version].replace("{document_text}", document_text)


def list_versions() -> list[str]:
    return list(PROMPTS.keys())
